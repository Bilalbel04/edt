Que es un join?
Un join en SQL es una operación que combina registros de dos o más tablas en base a una condición relacionada entre ellas. 
El resultado de un join es una tabla virtual que contiene columnas de ambas tablas, donde los registros coincidentes se combinan en una sola fila.

Como hacer un join?
SELECT columnas
FROM tabla1
JOIN tabla2 ON tabla1.columna_comun = tabla2.columna_comun;

